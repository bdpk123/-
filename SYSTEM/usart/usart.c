/**
  * @brief  串口1数据发送函数
  * @use 用于PC通讯
  * 
 */
void USART1_TX(u8 *data)
{
	while((*data)!='\0')
	{
	  
	  while((USART1->SR&0X40)==0);//循环发送,直到发送完毕 
		USART1->DR = *data; 
		data++;
	}
}

